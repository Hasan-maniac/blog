<p align="left" style="border:1px solid black;background-color:#00FFFF;height:27px;font-size:20px;margin-bottom:-7px;">About rules and moderation</p>
<p align="left">
    MIST blog is an open platform, an intermediary, where every blogger him/herself is fully responsible for what they publish under their account on MIST.  the authority of MIST respects the difference in opinions in the population, and we work to maintain debates that uphold the spirit of the independence of bangladesh, its cultural and social values, and keep watch against hate speech.  together with our bloggers, we have developed a set of rules which we use to avoid abuse of the freedom of expression and limit possible negative effects of postings done by any blogger.  we may moderate content and/or restrict access to post content from any account if we are made aware that it violates our rules,  and we inform the concerned blogger about such actions as far as possible..
</p>
<br>
<p align="left" style="border:1px solid black;background-color:#00FFFF;height:27px;margin-bottom:-7px;font-size:20px;">About requests to remove content</p>
<p align="left">
    while logged in, bloggers may report content or behaviour believed to be in violation of our rules through abuse reports.  any person may report by sending an email to feedback@MIST.net.  governments and organisations may request us to remove content for different reasons, be it allegations about anti-state, anti-religion, hate speech, illegal content, copyrighted content, defamation or other reasons.
</p>
